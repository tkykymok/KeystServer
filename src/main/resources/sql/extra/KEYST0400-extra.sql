SELECT
    KEYST0400.NOTIFICATION_ID
     ,KEYST0400.TITLE
     ,KEYST0400.CONTENT
     ,KEYST0400.POST_START_DATE
     ,KEYST0400.POST_END_DATE
     ,KEYST0400.NOTIFICATION_REG_DATETIME
     ,KEYST0100.USER_NAME AS NOTIFICATION_REG_PERSON_NAME
     ,KEYST0400.VERSION_EX_KEY
FROM KEYST0400
         INNER JOIN KEYST0100 ON
        KEYST0400.NOTIFICATION_REG_ID = KEYST0100.USER_ID
WHERE
        KEYST0400.DELETE_FLG = '0'
;
