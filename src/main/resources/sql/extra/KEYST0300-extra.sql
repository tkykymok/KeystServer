SELECT
	KEYST0300.RESERVE_ID
	,KEYST0300.IMPL_YEAR_MONTH
	,KEYST0300.MANAGER_ID
	,KEYST0300.TEAM
	,KEYST0320.RESERVE_DATE
	,KEYST0320.RESERVE_TIME
	,KEYST0310.USER_ID
	,KEYST0100.USER_NAME
FROM
	KEYST0300
LEFT OUTER JOIN KEYST0320 ON
	KEYST0300.RESERVE_ID = KEYST0320.RESERVE_ID
LEFT OUTER JOIN KEYST0310 ON
	KEYST0320.RESERVE_DATE = KEYST0310.RESERVE_DATE
AND
	KEYST0320.RESERVE_TIME = KEYST0310.RESERVE_TIME
LEFT OUTER JOIN KEYST0100 ON
	KEYST0310.USER_ID = KEYST0100.USER_ID
WHERE
	KEYST0300.MANAGER_ID = '17'
AND
	KEYST0300.IMPL_YEAR_MONTH = '202107';

